                            这也称作防御性注册，就是自己的商标在其他类别进行注册保护，防止他人抢注。比如说近年来比较火爆的“小米”等商标进行了全类别注册，而且类似于黑米、大米等相似商标都进行了注册。  

                            2、加强商标管理  

                            商标注册后，企业必须建立规范的商标管理制度，并指派专人办理商标注册、变更、延续等事宜。首先，要定期上网查看是否有其他企业申请与相关类别企业商标相同或相似的商标注册，并及时提出异议。二是及时处理商标事宜。在我城市或业由于管理上的疏忽而未能申请续展。结果商标过期，损失惨重。  

                            3、规范商标使用  

                            一些企业对LOGO与商标的关系不太清楚，在广告或产品包装中使用带有商标的LOGO，从而弱化了注册商标，未能更好地突出企业的商标品牌。此外，LOGO和商标的结合更不可取，因为它改变了注册商标所有人的行为，违反了《商标法》的规定。  

                            4、加大打击商标侵权的力度  

